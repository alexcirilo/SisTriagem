    public void pesquisar_consulta(){
        connection.ConnectionFactory.getConnection();
        String sql = "select pac.nomePessoa, cons.nomeConsulta, med.nomeMedico, cons.horario from sis_paciente pac inner join sis_tipoConsulta cons on pac.id = cons.paciente_id inner join sis_medico med on med.id = cons.medico_id  where nomepessoa like ? and nomeconsulta like ? and nomeMedico like ? and horario like ?";
        try {
            stmt = connection.ConnectionFactory.getConnection().prepareStatement(sql);
            stmt.setString(1, "%"+txPaciente.getText()+"%");
            stmt.setString(2, "%"+txConsulta.getText()+"%");
            stmt.setString(3, "%"+txMedico.getText()+"%");
            stmt.setString(4, "%"+txFormHorario.getText()+"%");
            
            rs = stmt.executeQuery();
            
            jTableConsulta.setModel(DbUtils.resultSetToTableModel(rs));
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }
